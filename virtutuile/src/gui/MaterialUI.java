public class MaterialUI {

    public String name;
    public String numberOfBoxes;
    public String tilePerBox;
    public String color;
    public String pricePerBoxe;
    public String totalPrice;

    public String getName() {
        return name;
    }

    public String getNumberOfBoxes() {
        return numberOfBoxes;
    }

    public String getTilePerBox() {
        return tilePerBox;
    }

    public String getColor() {
        return color;
    }

    public String getPricePerBoxe() {
        return pricePerBoxe;
    }

    public String getTotalPrice() {
        return totalPrice;
    }



}
